#ifndef	BINARYTREE_TYPES_H
#define	BINARYTREE_TYPES_H


/******************************************************************************* 
------------------------------- IMPORTS & PARAMS -------------------------------
*******************************************************************************/
#include <stddef.h>


/******************************************************************************* 
---------------------------------- TYPE DEFS -----------------------------------
*******************************************************************************/
typedef struct BTNode{
  int id;
  int val;
  struct BTNode *left;
  struct BTNode *right;
} BTNode;

typedef struct ITNode{
  int val;
  BTNode *node;
  struct ITNode *parent;
  struct ITNode *left;
  struct ITNode *right;
} ITNode;


/******************************************************************************* 
-------------------------------- FUNCTION DEFS ---------------------------------
*******************************************************************************/
void initBTNode(BTNode *node)
{
	node->id	= 0;
	node->val	= 0;
	node->left	= NULL;
	node->right	= NULL;
}

void initITNode(ITNode *node)
{
	node->val		= 0;
	node->node		= NULL;
	node->parent	= NULL;
	node->left		= NULL;
	node->right		= NULL;
}


#endif
